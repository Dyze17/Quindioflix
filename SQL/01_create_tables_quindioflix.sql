CREATE TABLE perfiles (
    id_perfil            NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_usuario           NUMBER         NOT NULL,
    nombre_perfil        VARCHAR2(60)   NOT NULL,
    avatar               VARCHAR2(120),
    tipo_perfil          VARCHAR2(10)   NOT NULL,
    fecha_creacion       DATE DEFAULT TRUNC(SYSDATE) NOT NULL,
    CONSTRAINT pk_perfiles PRIMARY KEY (id_perfil),
    CONSTRAINT uq_perfiles_usuario_nombre UNIQUE (id_usuario, nombre_perfil),
    CONSTRAINT fk_perfiles_usuario FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario),
    CONSTRAINT ck_perfiles_tipo CHECK (tipo_perfil IN ('ADULTO', 'INFANTIL'))
