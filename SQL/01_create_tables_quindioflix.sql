    CONSTRAINT uq_usuarios_email UNIQUE (email),
    CONSTRAINT fk_usr_plan FOREIGN KEY (id_plan_actual) REFERENCES planes(id_plan),
    CONSTRAINT fk_usr_referidor FOREIGN KEY (id_usuario_referidor) REFERENCES usuarios(id_usuario),
    CONSTRAINT ck_usr_estado CHECK (estado_cuenta IN ('ACTIVO', 'SUSPENDIDO', 'INACTIVO'))
