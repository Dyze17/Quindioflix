CREATE TABLE planes (
    id_plan              NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    nombre_plan          VARCHAR2(20)   NOT NULL,
    precio_mensual       NUMBER(10,2)   NOT NULL,
    max_pantallas        NUMBER(2)      NOT NULL,
    calidad_video        VARCHAR2(10)   NOT NULL,
    max_perfiles         NUMBER(2)      NOT NULL,
    descripcion          VARCHAR2(200),
    CONSTRAINT pk_planes PRIMARY KEY (id_plan),
    CONSTRAINT uq_planes_nombre UNIQUE (nombre_plan),
    CONSTRAINT ck_planes_nombre CHECK (nombre_plan IN ('BASICO', 'ESTANDAR', 'PREMIUM')),
    CONSTRAINT ck_planes_precio CHECK (precio_mensual > 0),
    CONSTRAINT ck_planes_pantallas CHECK (max_pantallas BETWEEN 1 AND 10),
    CONSTRAINT ck_planes_calidad CHECK (calidad_video IN ('SD', 'HD', '4K')),
    CONSTRAINT ck_planes_perfiles CHECK (max_perfiles BETWEEN 1 AND 10)
);

CREATE TABLE departamentos (
    id_departamento      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    nombre_departamento  VARCHAR2(40)   NOT NULL,
    descripcion          VARCHAR2(200),
    id_jefe              NUMBER,
    CONSTRAINT pk_departamentos PRIMARY KEY (id_departamento),
    CONSTRAINT uq_departamentos_nombre UNIQUE (nombre_departamento)
);

CREATE TABLE empleados (
    id_empleado          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_departamento      NUMBER         NOT NULL,
    supervisor_id        NUMBER,
    nombres              VARCHAR2(80)   NOT NULL,
    apellidos            VARCHAR2(80)   NOT NULL,
    correo               VARCHAR2(120)  NOT NULL,
    telefono             VARCHAR2(20),
    cargo                VARCHAR2(60)   NOT NULL,
    fecha_ingreso        DATE           NOT NULL,
    salario              NUMBER(12,2)   NOT NULL,
    estado_empleado      VARCHAR2(15)   DEFAULT 'ACTIVO' NOT NULL,
    CONSTRAINT pk_empleados PRIMARY KEY (id_empleado),
    CONSTRAINT uq_empleados_correo UNIQUE (correo),
    CONSTRAINT fk_emp_departamento FOREIGN KEY (id_departamento) REFERENCES departamentos(id_departamento),
    CONSTRAINT fk_emp_supervisor FOREIGN KEY (supervisor_id) REFERENCES empleados(id_empleado),
    CONSTRAINT ck_emp_estado CHECK (estado_empleado IN ('ACTIVO', 'INACTIVO')),
    CONSTRAINT ck_emp_salario CHECK (salario > 0)
);

ALTER TABLE departamentos
    ADD CONSTRAINT fk_dept_jefe
    FOREIGN KEY (id_jefe) REFERENCES empleados(id_empleado);

CREATE TABLE usuarios (
    id_usuario           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_plan_actual       NUMBER         NOT NULL,
    id_usuario_referidor NUMBER,
    nombres              VARCHAR2(80)   NOT NULL,
    apellidos            VARCHAR2(80)   NOT NULL,
    email                VARCHAR2(120)  NOT NULL,
