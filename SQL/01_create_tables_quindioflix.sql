CREATE TABLE categorias (
    id_categoria         NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    nombre_categoria     VARCHAR2(30)   NOT NULL,
    descripcion          VARCHAR2(200),
    CONSTRAINT pk_categorias PRIMARY KEY (id_categoria),
    CONSTRAINT uq_categorias_nombre UNIQUE (nombre_categoria)
);

CREATE TABLE generos (
    id_genero            NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    nombre_genero        VARCHAR2(40)   NOT NULL,
    descripcion          VARCHAR2(200),
    CONSTRAINT pk_generos PRIMARY KEY (id_genero),
    CONSTRAINT uq_generos_nombre UNIQUE (nombre_genero)
);

CREATE TABLE contenido (
    id_contenido             NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_categoria             NUMBER         NOT NULL,
    id_empleado_responsable  NUMBER         NOT NULL,
    titulo                   VARCHAR2(150)  NOT NULL,
    anio_lanzamiento         NUMBER(4)      NOT NULL,
    duracion_minutos         NUMBER(6)      NOT NULL,
    sinopsis                 VARCHAR2(1000),
    clasificacion_edad       VARCHAR2(5)    NOT NULL,
    fecha_agregado_catalogo  DATE           DEFAULT TRUNC(SYSDATE) NOT NULL,
    es_original              CHAR(1)        DEFAULT 'N' NOT NULL,
    popularidad              NUMBER(10)     DEFAULT 0 NOT NULL,
    estado_publicacion       VARCHAR2(15)   DEFAULT 'ACTIVO' NOT NULL,
    CONSTRAINT pk_contenido PRIMARY KEY (id_contenido),
    CONSTRAINT fk_cont_categoria FOREIGN KEY (id_categoria) REFERENCES categorias(id_categoria),
    CONSTRAINT fk_cont_empleado FOREIGN KEY (id_empleado_responsable) REFERENCES empleados(id_empleado),
    CONSTRAINT ck_cont_anio CHECK (anio_lanzamiento BETWEEN 1900 AND 2100),
    CONSTRAINT ck_cont_duracion CHECK (duracion_minutos > 0),
    CONSTRAINT ck_cont_edad CHECK (clasificacion_edad IN ('TP', '+7', '+13', '+16', '+18')),
    CONSTRAINT ck_cont_original CHECK (es_original IN ('S', 'N')),
    CONSTRAINT ck_cont_estado CHECK (estado_publicacion IN ('ACTIVO', 'INACTIVO'))
);

CREATE TABLE contenido_genero (
    id_contenido         NUMBER NOT NULL,
    id_genero            NUMBER NOT NULL,
    CONSTRAINT pk_contenido_genero PRIMARY KEY (id_contenido, id_genero),
    CONSTRAINT fk_cg_contenido FOREIGN KEY (id_contenido) REFERENCES contenido(id_contenido),
    CONSTRAINT fk_cg_genero FOREIGN KEY (id_genero) REFERENCES generos(id_genero)
);

CREATE TABLE temporadas (
    id_temporada         NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_contenido         NUMBER         NOT NULL,
    numero_temporada     NUMBER(3)      NOT NULL,
    titulo_temporada     VARCHAR2(120)  NOT NULL,
    fecha_lanzamiento    DATE,
    CONSTRAINT pk_temporadas PRIMARY KEY (id_temporada),
    CONSTRAINT uq_temporadas UNIQUE (id_contenido, numero_temporada),
    CONSTRAINT fk_temporadas_contenido FOREIGN KEY (id_contenido) REFERENCES contenido(id_contenido),
    CONSTRAINT ck_temporadas_num CHECK (numero_temporada > 0)
);

CREATE TABLE episodios (
    id_episodio          NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_temporada         NUMBER         NOT NULL,
    numero_episodio      NUMBER(4)      NOT NULL,
    titulo_episodio      VARCHAR2(150)  NOT NULL,
    duracion_minutos     NUMBER(6)      NOT NULL,
    fecha_publicacion    DATE,
    CONSTRAINT pk_episodios PRIMARY KEY (id_episodio),
    CONSTRAINT uq_episodios UNIQUE (id_temporada, numero_episodio),
    CONSTRAINT fk_episodios_temporada FOREIGN KEY (id_temporada) REFERENCES temporadas(id_temporada),
    CONSTRAINT ck_episodios_num CHECK (numero_episodio > 0),
    CONSTRAINT ck_episodios_duracion CHECK (duracion_minutos > 0)
);

CREATE TABLE contenido_relacionado (
    id_contenido_origen  NUMBER         NOT NULL,
    id_contenido_destino NUMBER         NOT NULL,
    tipo_relacion        VARCHAR2(30)   NOT NULL,
    descripcion          VARCHAR2(200),
    CONSTRAINT pk_contenido_rel PRIMARY KEY (id_contenido_origen, id_contenido_destino, tipo_relacion),
    CONSTRAINT fk_cr_origen FOREIGN KEY (id_contenido_origen) REFERENCES contenido(id_contenido),
    CONSTRAINT fk_cr_destino FOREIGN KEY (id_contenido_destino) REFERENCES contenido(id_contenido),
    CONSTRAINT ck_cr_tipo CHECK (tipo_relacion IN ('SECUELA', 'PRECUELA', 'REMAKE', 'SPIN_OFF', 'VERSION_EXTENDIDA', 'RELACIONADO')),
    CONSTRAINT ck_cr_distinto CHECK (id_contenido_origen <> id_contenido_destino)
);

CREATE TABLE pagos (
    id_pago              NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_usuario           NUMBER         NOT NULL,
    id_plan              NUMBER         NOT NULL,
    fecha_pago           DATE           NOT NULL,
    fecha_vencimiento    DATE           NOT NULL,
    monto_base           NUMBER(10,2)   NOT NULL,
    valor_descuento      NUMBER(10,2)   DEFAULT 0 NOT NULL,
    monto_pagado         NUMBER(10,2)   NOT NULL,
    metodo_pago          VARCHAR2(20)   NOT NULL,
    estado_pago          VARCHAR2(15)   NOT NULL,
    observacion          VARCHAR2(200),
    CONSTRAINT pk_pagos PRIMARY KEY (id_pago),
    CONSTRAINT fk_pagos_usuario FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario),
    CONSTRAINT fk_pagos_plan FOREIGN KEY (id_plan) REFERENCES planes(id_plan),
    CONSTRAINT ck_pagos_mbase CHECK (monto_base >= 0),
    CONSTRAINT ck_pagos_vdesc CHECK (valor_descuento >= 0),
    CONSTRAINT ck_pagos_mpagado CHECK (monto_pagado >= 0),
    CONSTRAINT ck_pagos_metodo CHECK (metodo_pago IN ('TCREDITO', 'TDEBITO', 'PSE', 'NEQUI', 'DAVIPLATA')),
    CONSTRAINT ck_pagos_estado CHECK (estado_pago IN ('EXITOSO', 'FALLIDO', 'PENDIENTE', 'REEMBOLSADO'))
);

CREATE TABLE reproducciones (
    id_reproduccion      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_perfil            NUMBER         NOT NULL,
    id_contenido         NUMBER         NOT NULL,
    id_episodio          NUMBER,
    fecha_hora_inicio    TIMESTAMP      NOT NULL,
    fecha_hora_fin       TIMESTAMP,
    dispositivo          VARCHAR2(20)   NOT NULL,
    porcentaje_avance    NUMBER(5,2)    DEFAULT 0 NOT NULL,
    CONSTRAINT pk_reproducciones PRIMARY KEY (id_reproduccion),
    CONSTRAINT fk_rep_perfil FOREIGN KEY (id_perfil) REFERENCES perfiles(id_perfil),
    CONSTRAINT fk_rep_contenido FOREIGN KEY (id_contenido) REFERENCES contenido(id_contenido),
    CONSTRAINT fk_rep_episodio FOREIGN KEY (id_episodio) REFERENCES episodios(id_episodio),
    CONSTRAINT ck_rep_dispositivo CHECK (dispositivo IN ('CELULAR', 'TABLET', 'TV', 'COMPUTADOR')),
    CONSTRAINT ck_rep_avance CHECK (porcentaje_avance BETWEEN 0 AND 100),
    CONSTRAINT ck_rep_fechas CHECK (fecha_hora_fin IS NULL OR fecha_hora_fin >= fecha_hora_inicio)
)
PARTITION BY RANGE (fecha_hora_inicio)
(
    PARTITION p_repro_2024 VALUES LESS THAN (TIMESTAMP '2025-01-01 00:00:00'),
    PARTITION p_repro_2025 VALUES LESS THAN (TIMESTAMP '2026-01-01 00:00:00'),
    PARTITION p_repro_max  VALUES LESS THAN (MAXVALUE)
);

CREATE TABLE calificaciones (
    id_calificacion      NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_perfil            NUMBER         NOT NULL,
    id_contenido         NUMBER         NOT NULL,
    estrellas            NUMBER(1)      NOT NULL,
    resena               VARCHAR2(1000),
    fecha_calificacion   DATE DEFAULT TRUNC(SYSDATE) NOT NULL,
    CONSTRAINT pk_calificaciones PRIMARY KEY (id_calificacion),
    CONSTRAINT uq_calif_perfil_contenido UNIQUE (id_perfil, id_contenido),
    CONSTRAINT fk_calif_perfil FOREIGN KEY (id_perfil) REFERENCES perfiles(id_perfil),
    CONSTRAINT fk_calif_contenido FOREIGN KEY (id_contenido) REFERENCES contenido(id_contenido),
    CONSTRAINT ck_calif_estrellas CHECK (estrellas BETWEEN 1 AND 5)
);

CREATE TABLE favoritos (
    id_perfil            NUMBER         NOT NULL,
    id_contenido         NUMBER         NOT NULL,
    fecha_agregado       DATE DEFAULT TRUNC(SYSDATE) NOT NULL,
    CONSTRAINT pk_favoritos PRIMARY KEY (id_perfil, id_contenido),
    CONSTRAINT fk_fav_perfil FOREIGN KEY (id_perfil) REFERENCES perfiles(id_perfil),
    CONSTRAINT fk_fav_contenido FOREIGN KEY (id_contenido) REFERENCES contenido(id_contenido)
);

CREATE TABLE reportes_contenido (
    id_reporte           NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    id_usuario_reporta   NUMBER         NOT NULL,
    id_contenido         NUMBER         NOT NULL,
    id_usuario_moderador NUMBER,
    fecha_reporte        DATE DEFAULT TRUNC(SYSDATE) NOT NULL,
    motivo               VARCHAR2(200)  NOT NULL,
    estado_reporte       VARCHAR2(15)   DEFAULT 'PENDIENTE' NOT NULL,
    fecha_resolucion     DATE,
    decision_moderacion  VARCHAR2(200),
    CONSTRAINT pk_reportes PRIMARY KEY (id_reporte),
    CONSTRAINT fk_reporta_usuario FOREIGN KEY (id_usuario_reporta) REFERENCES usuarios(id_usuario),
    CONSTRAINT fk_reporta_contenido FOREIGN KEY (id_contenido) REFERENCES contenido(id_contenido),
    CONSTRAINT fk_reporta_moderador FOREIGN KEY (id_usuario_moderador) REFERENCES usuarios(id_usuario),
    CONSTRAINT ck_reportes_estado CHECK (estado_reporte IN ('PENDIENTE', 'EN_REVISION', 'RESUELTO', 'RECHAZADO'))
);

